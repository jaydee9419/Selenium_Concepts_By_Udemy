"""
collections means storing multiple variable values in a single variable
--------------------
1. List - []
2. Tuple - ()
3. Set - {}
4. Dictionary - {}
"""
# 1. List - []
# =======================================
# Syntax: my_list = [1, 2, 3, 'hello']
# Lists are mutable, meaning you can modify their contents by adding, removing, or changing elements.
# Example:
my_list = [1, 2, 3]
my_list.append(4)
my_list[0] = 100
print(my_list)


