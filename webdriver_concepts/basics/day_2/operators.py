#1. Arthmetic operator (like +, -, *, /)

a = 17
b = 2

print(a+b) #additional
print(a-b) #Substraction
print(a*b) #Multiplication

print(a/b) # Division with fraction value
print(a//b) # complate division
print(a%b) # moduler division (1 reminder)
print(a**b) # power


# 2. Relational operator (like >, <, >=, <=, ==, !=)
#note: this opetaions always returns boolen values(True/False)
a = 10
b = 15

print(a > b)
print(a < b)
print(a >= b)
print(a <= b)
print(a == b)
print(a != b)

# 3. Logical (like and, or, not)
#note: this opetaions always returns boolen values(True/False)
a = True
b = False

print(a and b)
print(a or b)
