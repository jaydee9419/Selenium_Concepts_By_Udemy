# in simple terms joining of two variables, strings and etc.
print(10 + 20) #valid 
print(10.1 + 20.4) #valid 
print(10 + 20.5) #valid 

print('Hello' + 'World') #valid 

print(True + 6) #True consider as 1 #valid 
print(False + 6) #False consider as 0 #valid 
print(True + True) #valid 
print(False + False) #valid 

print("10" + "20") #valid 
print("10" + 20) #invalid (both are diffrent values)
print("10" + 20.5) #invalid (both are diffrent values)
print(True + 'Welcome') #invalid (both are diffrent values)










